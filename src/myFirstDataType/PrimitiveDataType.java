package myFirstDataType;

public class PrimitiveDataType {
    public static void main(String[] args) {
        boolean redButton = true;
        char firstCharacter = 'a';
        byte b = 34;
        short c = 3344;
        int myNumber = 465785888;
        long myLongNum = 808086778778887789L;
        float myFloatNum = 5346.2345678f;
        double myDoubleNum = 66.879778777d;

        System.out.println(redButton);
        System.out.println(firstCharacter);
        System.out.println(b);
        System.out.println(c);
        System.out.println(myNumber);
        System.out.println(myLongNum);
        System.out.println(myFloatNum);
        System.out.println(myDoubleNum);
    }
}
