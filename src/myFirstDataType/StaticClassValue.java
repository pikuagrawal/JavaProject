package myFirstDataType;

public class StaticClassValue {
    static boolean isCheckoutButton = true;
    static char myLuckyChar = 'b';
    static byte myByteNum;
    static short myShortNum;
    static int myIntNum;
    static long myLongNum;


    public static void main(String[] args) {
        System.out.println(isCheckoutButton);
        System.out.println(myLuckyChar);
        System.out.println(myByteNum);
        System.out.println(myShortNum);
        System.out.println(myIntNum);
        System.out.println(myLongNum);
    }
}
