package myFirstDataType;

public class AllDataType {
    public static void main(String[] args) {
        boolean redButton = true;
        System.out.println(redButton);
        redButton = false; // reassign boolean value
        System.out.println(redButton);

        char firstCharacter = 'a';
        System.out.println(firstCharacter);
        firstCharacter = 'e'; // reassign character value
        System.out.println(firstCharacter);

        byte b = 34;
        System.out.println(b);
        b = 35; // reassign byte value
        System.out.println(b);

        short c = 3344;
        System.out.println(c);
        c = 7689; // reassign short value
        System.out.println(c);

        int myNumber = 465785888;
        System.out.println(myNumber);
        myNumber = 768768698; // reassign integer value
        System.out.println(myNumber);

        long myLongNum = 808086778778887789L;
        System.out.println(myLongNum);
        myLongNum = 564754765876986798L; // reassign long value
        System.out.println(myLongNum);

        float myFloatNum = 5346.14515f;
        System.out.println(myFloatNum);
        myFloatNum = 7878.576f; // reassign float value
        System.out.println(myFloatNum);

        double myDoubleNum = 66.879778777d;
        System.out.println(myDoubleNum);
        myDoubleNum = 8778.89797987987987868d;// reassign double value
        System.out.println(myDoubleNum);

        String myName = "Priyanka";
        System.out.println(myName);
        myName= "piku";// reassign string value
        System.out.println(myName);
    }
}
